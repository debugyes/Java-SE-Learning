/*
    不同进制的数据表现：
        二进制：由01组成，以0b开头
        八进制：由0-7组成，以0开头
        十进制：整数默认十进制
        十六进制：以0-f组成，以0x开头
* */
public class Decimal {
    public static void main(String[] args){
        //十进制
        System.out.println(100);

        //二进制
        System.out.println(0b100);

        //八进制
        System.out.println(0100);

        //十六进制
        System.out.println(0x100);
        //输出都是十进制
         
    }
}
